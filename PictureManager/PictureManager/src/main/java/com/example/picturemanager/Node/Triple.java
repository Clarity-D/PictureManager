package com.example.picturemanager.Node;

public class Triple
{
    private String one;
    private String two;
    private String three;

    public Triple(String one,String two,String three)
    {
        this.one=one;
        this.two=two;
        this.three=three;
    }

    public String getOne()
    {
        return one;
    }

    public String getTwo()
    {
        return two;
    }

    public String getThree()
    {
        return three;
    }
}
